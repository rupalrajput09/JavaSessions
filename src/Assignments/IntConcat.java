package Assignments;

public class IntConcat {

	public static void main(String[] args) {

		int num1 = 100;
		int num2 = 200;
		int num3 = 3400;
		
		int addition = num1+num2+num3;
		
		System.out.println("Adding three variables : " + addition);
		
		String str = "Your total amount is. ";
		
		System.out.println("Output :");
		System.out.println(str + addition);
		
	}

}
